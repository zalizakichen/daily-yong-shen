export function getDayMasterInfo(dayStem: string): DayMasterInfo {
  const info = DAY_MASTER_MAP[dayStem];
  if (!info) {
    return {
      stem: dayStem,
      element: "",
      title: `你是${dayStem}`,
      quote: "",
      description: "",
    };
  }
  return {
    stem: dayStem,
    element: info.element,
    title: `你是${dayStem}${info.element}`,
    quote: info.quote,
    description: info.description,
  };
}
